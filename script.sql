---------PROGRAM
CREATE TABLE PROGRAM(
    ID_PROGRAM NUMBER(5,0) PRIMARY KEY,
    TIP_PROGRAM VARCHAR2(20) NOT NULL,
        CONSTRAINT CHECK_PROG_TIP CHECK (TIP_PROGRAM IN ('CONSULTATIE','GARDA','OPERATIE','URGENTE')),
    ORA_START NUMBER(2,0) NOT NULL,
    MIN_START NUMBER(2,0) NOT NULL,
    ORA_SFARSIT NUMBER(2,0) NOT NULL,
    MIN_SFARSIT NUMBER(2,0) NOT NULL,
    CONSTRAINT CHECK_ORA_INTERVAL CHECK (
        (ORA_START BETWEEN 0 AND 23) AND
        (MIN_START BETWEEN 0 AND 59) AND
        (ORA_SFARSIT BETWEEN 0 AND 23) AND
        (MIN_SFARSIT BETWEEN 0 AND 59)
    )
);


---------DOCTOR
CREATE TABLE DOCTOR (
    CNP_DOCTOR NUMBER(13,0) PRIMARY KEY,
        CONSTRAINT CHECK_DOC_CNP CHECK (LENGTH(CNP_DOCTOR)=13),
    NUME_DOCTOR VARCHAR2(30) NOT NULL,
    PRENUME_DOCTOR VARCHAR2(30) NOT NULL,
    SPECIALIZARE VARCHAR2(30) NOT NULL,
    NR_TELEFON VARCHAR2(10) NOT NULL,
    ID_PROGRAM NUMBER(5,0),
    ID_DEPARTAMENT NUMBER(5,0) NOT NULL,
    CONSTRAINT DOC_FR_KEY_PROG FOREIGN KEY(ID_PROGRAM) 
        REFERENCES PROGRAM(ID_PROGRAM) ON DELETE SET NULL,
    CONSTRAINT DOC_FR_KEY_DEP FOREIGN KEY(ID_DEPARTAMENT) 
        REFERENCES DEPARTAMENT(ID_DEPARTAMENT) ON DELETE CASCADE
);
ALTER TABLE DOCTOR 
ADD CONSTRAINT DOC_TEL_UNQ UNIQUE(NR_TELEFON);
ALTER TABLE DOCTOR DROP CONSTRAINT CHECK_DOC_CNP;
ALTER TABLE DOCTOR
ADD CONSTRAINT CHECK_DOC_CNP CHECK (LENGTH(CNP_DOCTOR)=13 AND SUBSTR(CNP_DOCTOR,1,1) IN ('1','2','5','6'));


---------PACIENT
